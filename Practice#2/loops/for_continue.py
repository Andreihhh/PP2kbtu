# 1 example
for i in range(5):
    if i == 2:
        continue
    print(i)

# 2 example
for x in range(6):
    if x % 2 == 0:
        continue
    print(x)

# 3 example
for char in "python":
    if char == "h":
        continue
    print(char)

# 4 example
for i in range(1, 6):
    if i == 4:
        continue
    print(i)

# 5 example
for num in [1, 2, 3, 4]:
    if num == 3:
        continue
    print(num)
